#!/usr/bin/env node
// ============================================================================
// Generate supabase/seed.sql from SEED_POSTS + STATE_SEED_POSTS in constants.js
//
// Re-run any time the seed data changes:
//   node scripts/generate-seed-sql.mjs
//
// Then paste the generated SQL into the Supabase SQL editor (or run via psql).
// The generated SQL is idempotent — uses ON CONFLICT (seed_key) DO NOTHING.
// ============================================================================

import { SEED_POSTS, STATE_SEED_POSTS } from '../src/constants.js'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '..')
const outPath = path.join(repoRoot, 'supabase', 'seed.sql')

// Escape SQL string literal.
const lit = (s) => s == null ? 'NULL' : `'${String(s).replace(/'/g, "''")}'`
// Escape JSON for jsonb column literal.
const litJson = (v) => (v == null || (Array.isArray(v) && v.length === 0))
  ? 'NULL'
  : `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`
const num = (n) => n == null ? 'NULL' : Number(n)

const lines = []
lines.push('-- =========================================================================')
lines.push('-- Pulse seed data — auto-generated by scripts/generate-seed-sql.mjs')
lines.push('-- Run AFTER applying supabase/schema.sql. Idempotent (ON CONFLICT).')
lines.push('-- =========================================================================')
lines.push('')
lines.push('BEGIN;')
lines.push('')

const allSeeds = [...SEED_POSTS, ...STATE_SEED_POSTS]

for (const post of allSeeds) {
  const seedKey = `pulse-seed-post-${post.id}`
  const seedAuthor = post.incognito ? null : (post.author || null)
  const commentCount = (post.comments || []).length

  // Random "created N days ago" between 0-7 for visual variety.
  const ageDays = Math.floor(Math.random() * 7)
  const ageHours = Math.floor(Math.random() * 24)

  // comment_count is intentionally omitted — the `adjust_post_comment_count`
  // trigger increments it for each comment we insert below, so pre-seeding
  // would double-count.
  lines.push(`-- ${post.scope === 'state' ? '[state]' : '[local]'} ${post.title.slice(0, 70)}`)
  lines.push(`INSERT INTO posts (`)
  lines.push(`  seed_key, type, scope, title, description, category, location_label,`)
  lines.push(`  lat, lng, is_incognito, vote_count, media, impact,`)
  lines.push(`  seed_author, created_at`)
  lines.push(`) VALUES (`)
  lines.push(`  ${lit(seedKey)},`)
  lines.push(`  ${lit(post.type || 'statement')},`)
  lines.push(`  ${lit(post.scope || 'local')},`)
  lines.push(`  ${lit(post.title)},`)
  lines.push(`  ${lit(post.description)},`)
  lines.push(`  ${lit(post.category)},`)
  lines.push(`  ${lit(post.location)},`)
  lines.push(`  ${num(post.lat)}, ${num(post.lng)},`)
  lines.push(`  ${post.incognito ? 'true' : 'false'},`)
  lines.push(`  ${post.votes || 0},`)
  lines.push(`  ${litJson(post.media)},`)
  lines.push(`  ${litJson(post.impact)},`)
  lines.push(`  ${lit(seedAuthor)},`)
  lines.push(`  NOW() - INTERVAL '${ageDays} days ${ageHours} hours'`)
  lines.push(`) ON CONFLICT (seed_key) DO NOTHING;`)
  lines.push('')

  for (const c of (post.comments || [])) {
    const commentSeedKey = `pulse-seed-comment-${post.id}-${c.id}`
    const cAuthor = c.incognito ? null : (c.author || null)
    const cHours = Math.floor(Math.random() * 48) + 1
    lines.push(`INSERT INTO comments (`)
    lines.push(`  seed_key, post_id, text, is_incognito, vote_count, seed_author, created_at`)
    lines.push(`)`)
    lines.push(`SELECT ${lit(commentSeedKey)}, p.id, ${lit(c.text)}, ${c.incognito ? 'true' : 'false'},`)
    lines.push(`  ${c.votes || 0}, ${lit(cAuthor)}, NOW() - INTERVAL '${cHours} hours'`)
    lines.push(`FROM posts p WHERE p.seed_key = ${lit(seedKey)}`)
    lines.push(`ON CONFLICT (seed_key) DO NOTHING;`)
    lines.push('')
  }
}

lines.push('COMMIT;')
lines.push('')

fs.writeFileSync(outPath, lines.join('\n'))
console.log(`✓ Wrote ${outPath}`)
console.log(`  ${allSeeds.length} posts (${SEED_POSTS.length} local + ${STATE_SEED_POSTS.length} state)`)
console.log(`  ${allSeeds.reduce((s, p) => s + (p.comments?.length || 0), 0)} comments`)
